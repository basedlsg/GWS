/**
 * Portfolio Export Utilities
 * Handle exporting portfolios to various formats
 */

import type { Portfolio, Achievement } from '../types';
import { CATEGORY_INFO, STATUS_INFO } from '../constants';

/**
 * Export portfolio as Markdown
 */
export function exportPortfolioAsMarkdown(portfolio: Portfolio): string {
  const { title, description, achievements } = portfolio;

  let markdown = `# ${title}\n\n`;
  markdown += `${description}\n\n`;
  markdown += `---\n\n`;

  // Group by category
  const categories = [...new Set(achievements.map((a) => a.category))];

  categories.forEach((category) => {
    const categoryAchievements = achievements.filter((a) => a.category === category);
    if (categoryAchievements.length === 0) return;

    const categoryInfo = CATEGORY_INFO[category];
    markdown += `## ${categoryInfo.icon} ${categoryInfo.label}\n\n`;

    categoryAchievements.forEach((achievement) => {
      markdown += formatAchievementMarkdown(achievement);
      markdown += `\n`;
    });
  });

  markdown += `---\n\n`;
  markdown += `*Generated by The Great Work Suite - Culmination*\n`;

  return markdown;
}

/**
 * Format single achievement as markdown
 */
function formatAchievementMarkdown(achievement: Achievement): string {
  const statusInfo = STATUS_INFO[achievement.status];

  let md = `### ${statusInfo.icon} ${achievement.title}\n\n`;
  md += `**Status:** ${statusInfo.label}\n`;
  md += `**Target Date:** ${new Date(achievement.targetDate).toLocaleDateString()}\n`;

  if (achievement.completionDate) {
    md += `**Completed:** ${new Date(achievement.completionDate).toLocaleDateString()}\n`;
  }

  if (achievement.tags.length > 0) {
    md += `**Tags:** ${achievement.tags.map((t) => `#${t}`).join(' ')}\n`;
  }

  md += `\n${achievement.description}\n`;

  if (achievement.metrics && achievement.metrics.length > 0) {
    md += `\n**Key Metrics:**\n\n`;
    achievement.metrics.forEach((metric) => {
      md += `- ${metric.icon || '•'} **${metric.label}:** ${metric.value}\n`;
    });
  }

  if (achievement.narrative) {
    md += `\n**Success Story:**\n\n${achievement.narrative}\n`;
  }

  return md;
}

/**
 * Export portfolio as JSON
 */
export function exportPortfolioAsJSON(portfolio: Portfolio): string {
  return JSON.stringify(portfolio, null, 2);
}

/**
 * Export portfolio as HTML
 */
export function exportPortfolioAsHTML(portfolio: Portfolio): string {
  const { title, description, achievements, theme } = portfolio;

  let html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHtml(title)}</title>
  <style>
    ${getPortfolioCSS(theme)}
  </style>
</head>
<body class="theme-${theme}">
  <div class="portfolio-container">
    <header class="portfolio-header">
      <h1>${escapeHtml(title)}</h1>
      <p class="portfolio-description">${escapeHtml(description)}</p>
    </header>

    <main class="achievements-grid">
`;

  achievements.forEach((achievement) => {
    html += formatAchievementHTML(achievement);
  });

  html += `
    </main>

    <footer class="portfolio-footer">
      <p>Generated by The Great Work Suite - Culmination</p>
      <p>${new Date().toLocaleDateString()}</p>
    </footer>
  </div>
</body>
</html>`;

  return html;
}

/**
 * Format single achievement as HTML card
 */
function formatAchievementHTML(achievement: Achievement): string {
  const categoryInfo = CATEGORY_INFO[achievement.category];
  const statusInfo = STATUS_INFO[achievement.status];

  return `
    <div class="achievement-card" data-category="${achievement.category}" data-status="${achievement.status}">
      <div class="achievement-header">
        <span class="achievement-category">${categoryInfo.icon} ${categoryInfo.label}</span>
        <span class="achievement-status status-${achievement.status}">${statusInfo.icon} ${statusInfo.label}</span>
      </div>

      <h2 class="achievement-title">${escapeHtml(achievement.title)}</h2>

      <p class="achievement-description">${escapeHtml(achievement.description)}</p>

      ${achievement.imageUrl ? `<img src="${escapeHtml(achievement.imageUrl)}" alt="${escapeHtml(achievement.title)}" class="achievement-image" />` : ''}

      ${achievement.metrics && achievement.metrics.length > 0 ? `
        <div class="achievement-metrics">
          ${achievement.metrics.map((m) => `
            <div class="metric">
              <span class="metric-icon">${m.icon || '•'}</span>
              <span class="metric-label">${escapeHtml(m.label)}</span>
              <span class="metric-value">${escapeHtml(m.value)}</span>
            </div>
          `).join('')}
        </div>
      ` : ''}

      ${achievement.tags.length > 0 ? `
        <div class="achievement-tags">
          ${achievement.tags.map((tag) => `<span class="tag">#${escapeHtml(tag)}</span>`).join('')}
        </div>
      ` : ''}

      <div class="achievement-dates">
        <span>Target: ${new Date(achievement.targetDate).toLocaleDateString()}</span>
        ${achievement.completionDate ? `<span>Completed: ${new Date(achievement.completionDate).toLocaleDateString()}</span>` : ''}
      </div>

      ${achievement.narrative ? `
        <details class="achievement-narrative">
          <summary>Success Story</summary>
          <p>${escapeHtml(achievement.narrative)}</p>
        </details>
      ` : ''}
    </div>
  `;
}

