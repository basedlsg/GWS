/**
 * Export portfolio as plain text
 */
export function exportPortfolioAsText(portfolio: Portfolio): string {
  const { title, description, achievements } = portfolio;

  let text = `${title.toUpperCase()}\n`;
  text += `${'='.repeat(title.length)}\n\n`;
  text += `${description}\n\n`;
  text += `${'-'.repeat(60)}\n\n`;

  achievements.forEach((achievement) => {
    const categoryInfo = CATEGORY_INFO[achievement.category];
    const statusInfo = STATUS_INFO[achievement.status];

    text += `${categoryInfo.icon} ${achievement.title}\n`;
    text += `Category: ${categoryInfo.label} | Status: ${statusInfo.label}\n`;
    text += `Target: ${new Date(achievement.targetDate).toLocaleDateString()}\n`;

    if (achievement.completionDate) {
      text += `Completed: ${new Date(achievement.completionDate).toLocaleDateString()}\n`;
    }

    text += `\n${achievement.description}\n`;

    if (achievement.metrics && achievement.metrics.length > 0) {
      text += `\nMetrics:\n`;
      achievement.metrics.forEach((metric) => {
        text += `  ${metric.icon || '•'} ${metric.label}: ${metric.value}\n`;
      });
    }

    if (achievement.tags.length > 0) {
      text += `\nTags: ${achievement.tags.map((t) => `#${t}`).join(' ')}\n`;
    }

    text += `\n${'-'.repeat(60)}\n\n`;
  });

  text += `Generated by The Great Work Suite - Culmination\n`;
  text += `${new Date().toLocaleDateString()}\n`;

  return text;
}

/**
 * Download content as file
 */
export function downloadFile(content: string, filename: string, mimeType: string): void {
  const blob = new Blob([content], { type: mimeType });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = sanitizeFilename(filename);
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

/**
 * Copy to clipboard
 */
export async function copyToClipboard(text: string): Promise<boolean> {
  try {
    await navigator.clipboard.writeText(text);
    return true;
  } catch (error) {
    console.error('Failed to copy to clipboard:', error);
    return false;
  }
}

/**
 * Sanitize filename for download
 */
function sanitizeFilename(filename: string): string {
  return filename
    .replace(/[^a-z0-9_\-\.]/gi, '_')
    .replace(/_+/g, '_')
    .substring(0, 100);
}

/**
 * Escape HTML special characters
 */
function escapeHtml(text: string): string {
  const div = document.createElement('div');
  div.textContent = text;
  return div.innerHTML;
}
